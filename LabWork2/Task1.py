# Читаем входную строку и разбиваем на список чисел
print("Введите входную строку (через пробел): ")
numbers = input().split()

# Создаем множество для хранения уже встреченных чисел
seen = set()

# Проходим по каждому числу в списке
for num_str in numbers:
    # Преобразуем строку в целое число
    num = int(num_str)

    # Проверяем, встречалось ли число ранее
    if num in seen:
        print("YES")
    else:
        print("NO")
        # Добавляем число в множество
        seen.add(num)